{ stringstream s; s << x; int r; s >> r; return r;}

#define ll long long

int main() {
  ll m, n;
  cin >> m >> n;
  if((n - 1) >= (m - n)) cout << max(1LL, n - 1LL) << endl;
  else cout << min(m, n + 1LL) << endl;
  return 0;
}
